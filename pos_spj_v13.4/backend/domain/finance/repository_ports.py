from backend.domain.finance.entities.fixed_asset import FixedAsset
from backend.domain.finance.entities.journal import Journal
from backend.domain.finance.entities.journal_entry import JournalEntry
from backend.domain.finance.entities.payable import Payable, SupplierPayment
from backend.domain.finance.entities.posting_profile import PostingProfile
from backend.domain.finance.entities.receivable import Collection, Receivable
from backend.domain.finance.entities.reconciliation import Reconciliation
from backend.domain.finance.entities.treasury_account import TreasuryAccount
from backend.domain.finance.enums import (
    CommercialInstrumentType,
    JournalType,
    PostingPurpose,
)


class AccountRepositoryPort(Protocol):
    def save(self, account: Account) -> None: ...
    def get(self, account_id: str) -> Account | None: ...
    def get_by_code(self, code: str) -> Account | None: ...
    def list_active(self) -> list[Account]: ...
    def list_all(self) -> list[Account]: ...


class JournalRepositoryPort(Protocol):
    def save(self, journal: Journal) -> None: ...
    def get(self, journal_id: str) -> Journal | None: ...
    def get_by_type(self, journal_type: JournalType) -> Journal | None: ...
    def list_all(self) -> list[Journal]: ...
    def next_entry_number(self, journal: Journal) -> str: ...


class JournalEntryRepositoryPort(Protocol):
    def save(self, entry: JournalEntry) -> None: ...
    def update_status(self, entry: JournalEntry) -> None: ...
    def get(self, entry_id: str) -> JournalEntry | None: ...
    def find_by_posting_reference(
        self, source_module: str, source_document_id: str, posting_purpose: PostingPurpose,
    ) -> JournalEntry | None: ...
    def find_by_operation_id(self, operation_id: str) -> JournalEntry | None: ...
    def exists_unbalanced(self) -> bool: ...


