"""Repository ports for the finance bounded context.

Protocols only — implementations live in
``backend/infrastructure/db/repositories/finance/``. Every id is a UUIDv7
string; every amount crossing a port is a ``Money`` value object.
"""

from __future__ import annotations

from datetime import date
from typing import Protocol

from backend.domain.finance.entities.account import Account
from backend.domain.finance.entities.bank_statement import BankStatement
from backend.domain.finance.entities.budget import Budget
from backend.domain.finance.entities.commercial_obligation import CommercialObligation
